#include "libft.h"

int	ft_nbrlen(int n)
{
	int res;

	res = 0;
	if (n == 0)
		return (1);
	if (n < 0)
		res++;
	while (n)
	{
		res++;
		n = n / 10;
	}
	return (res);
}